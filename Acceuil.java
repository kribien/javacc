    private void enregistrerClient() {
       
        String nom = jTextName.getText();
        String prenom = jTextLName.getText();
        String sexe =  (String) ComboBoxSEXE.getSelectedItem();
        String address = Address.getText();
        
        String telephone = telField.getText();
        String url = "jdbc:mysql://localhost:3306/gestiocommande";
        String utilisateur = "root";
        String motDePasse = "";

        String sql = "INSERT INTO clients (nomclient, prenomclient,genreclient,telclient,addresseclient) VALUES (?, ?, ?, ?, ?)";

        try (Connection conn = DriverManager.getConnection(url, utilisateur, motDePasse);
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, nom);
            stmt.setString(2, prenom);
            stmt.setString(3, sexe);
            stmt.setString(4, telephone);
            stmt.setString(5, address);

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Client enregistré avec succès !");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erreur : " + ex.getMessage());
        }
    
      }
    
    
    
